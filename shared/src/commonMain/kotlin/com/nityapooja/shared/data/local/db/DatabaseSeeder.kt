    suspend fun seed() {
        val seededVersion = preferencesManager.getSeededVersion()
        if (seededVersion >= CONTENT_VERSION) return

        seedDeities()
        seedAartis()
        seedStotrams()
        seedKeertanalu()
        seedMantras()
        seedBhajans()
        seedSuprabhatam()
        seedAshtotra()
        seedTemples()
        seedFestivals()
        seedShlokas()
        seedChalisas()
        seedRashis()
        seedPujaSteps()

        preferencesManager.setSeededVersion(CONTENT_VERSION)
    }

